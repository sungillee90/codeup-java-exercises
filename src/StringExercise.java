public class StringExercise {
    public static void main (String[] arg) {

        String firstMoto = "We don't need no education. \nWe don't need no thought control.";
        System.out.println(firstMoto);

        String output = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(output);

        String output2 = "In windows, the main drive is usually C:\\"; // "\" backslash
        System.out.println(output2);

        String secondMoto = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(secondMoto);
    }
}
